package com.safeplatform.samples.web.dao;

import java.util.Date;

public class CollisionList {
    private Integer id;

    private String vin;

    private String state;

    private String locationinfo;

    private String collisioninfo;

    private String descriptioninfo;



    private Date time;


    //--------
    private String brand;

    private String mod;

    private String name;

    private String phone;





    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getVin() {
        return vin;
    }

    public void setVin(String vin) {
        this.vin = vin;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getLocationinfo() {
        return locationinfo;
    }

    public void setLocationinfo(String locationinfo) {
        this.locationinfo = locationinfo;
    }

    public String getCollisioninfo() {
        return collisioninfo;
    }

    public void setCollisioninfo(String collisioninfo) {
        this.collisioninfo = collisioninfo;
    }

    public String getDescriptioninfo() {
        return descriptioninfo;
    }

    public void setDescriptioninfo(String descriptioninfo) {
        this.descriptioninfo = descriptioninfo;
    }


    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }





    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getMod() {
        return mod;
    }

    public void setMod(String mod) {
        this.mod = mod;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }




}